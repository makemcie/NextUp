export type Lang = "es" | "en";

export function getConsentText(lang: Lang, businessName: string): string {
	if (lang === "en") {
		return `By checking this box, I agree to receive SMS messages from ${businessName} about my place in line, service updates, and occasional promotions. Msg & data rates may apply. Message frequency may vary. Reply STOP to opt out or HELP for assistance. Consent is not a condition of purchase.`;
	}
	return `Al marcar esta casilla, acepto recibir mensajes de texto (SMS) de ${businessName} sobre mi lugar en la fila, actualizaciones de servicio y promociones ocasionales. Pueden aplicar tarifas de mensajes y datos. La frecuencia de mensajes puede variar. Responde STOP para cancelar o HELP para asistencia. El consentimiento no es condición de compra.`;
}

